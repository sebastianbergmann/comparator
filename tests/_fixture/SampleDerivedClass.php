namespace SebastianBergmann\Comparator;

/**
 * A sample derived class with a private member redeclared.
 */
class SampleDerivedClass extends SampleClass
{
    public $a;

    protected $b;

    private $c;

    public function __construct($a, $b, $c)
    {
        $this->a = $a;
        $this->b = $b;
        $this->c = $c;
    }
}
